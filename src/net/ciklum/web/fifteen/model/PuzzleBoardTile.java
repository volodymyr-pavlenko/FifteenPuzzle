package net.ciklum.web.fifteen.model;

public class PuzzleBoardTile {
	private int v;
	private boolean canmove;

	public int getV() {
		return v;
	}

	public void setV(int v) {
		this.v = v;
	}

	public boolean isCanmove() {
		return canmove;
	}

	public void setCanmove(boolean canmove) {
		this.canmove = canmove;
	}

}
